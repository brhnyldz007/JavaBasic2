package com.neotech.lesson03;

public class Task {
	public static void main(String[] args) {
		
		//declare variable
		int a = 100;
		//increase by shorthand operator
		a += 100;
		//Print variable
		System.out.println(a);
		
		System.out.println();
		
		
		//declare variable
		int b = 200;
		//decrease by shorthand operator
		b -= 100;
		//Print variable
		System.out.println(b);
		
		System.out.println();
		
		//declare variable
		int CakePiece = 11;
		//divided by shorthand operator
		CakePiece /=4;
		//Print variable
		System.out.println(CakePiece);
		
		System.out.println();
		
		//declare variable
		int CakePiece1 = 25;
		//remainder assignment by shorthand operator
		CakePiece1 %=7;
		//Print variable
		System.out.println(CakePiece1);
		
	}

}
